package se.linus;

public class calculator {
    public int addition(int a, int b) {
        return a + b;
    }

    public double divide(double a, double b) {
        return a / b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int multiplication(int a, int b) {
        return a * b;
    }
}
